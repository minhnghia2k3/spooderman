func NewSpoodermanCommand() *cobra.Command {
	short := fmt.Sprintf("%s  - personal AI assistant v%s\n\n", pkg.Logo, config.Version)

	cmd := &cobra.Command{
		Use:     "spooderman",
		Short:   short,
		Example: "spooderman version",
	}

	cmd.AddCommand(
		version.NewVersionCommand(),
	)

	return cmd
}

